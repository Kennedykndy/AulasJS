// Tipagem Dinâmica

let outraVar = 567; // A variável "outraVar" é do tipo number
console.log(typeof outraVariavel); // O operador "typeof" é usado para verificar o tipo de uma variável. Ele retorna uma string indicando o tipo da variável.

outraVar = "Agora sou uma string"; // A variável "outraVar" agora é do tipo string, pois foi atribuída uma string a ela.
console.log(typeof outraVariavel); // O operador "typeof" retorna "string" para a variável "outraVar", indicando que ela agora é do tipo string.

outraVar = true; // A variável "outraVar" agora é do tipo boolean, pois foi atribuída um valor booleano (true) a ela.
console.log(typeof outraVariavel); // O operador "typeof" retorna "boolean" para a variável "outraVar", indicando que ela agora é do tipo boolean.

outraVar = { nome: "Kennedy" }; // A variável "outraVar" agora é do tipo object, pois foi atribuída um objeto a ela.
console.log(typeof outraVariavel); // O operador "typeof" retorna "object" para a variável "outraVar", indicando que ela agora é do tipo object.

outraVar = [1, 2, 3]; // A variável "outraVar" agora é do tipo object, pois foi atribuída um array a ela.
console.log(typeof outraVar); // O operador "typeof" retorna "object" para a variável "outraVar", indicando que ela agora é do tipo object, mesmo sendo um array.
